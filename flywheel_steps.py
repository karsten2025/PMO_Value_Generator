def get_step_by_id(step_id):
    """Gibt einen spezifischen Schritt anhand seiner ID zurück"""
    return flywheel_steps.get(step_id)


def get_steps_by_stage(stage_name):
    """Gibt alle Schritte einer bestimmten Stage zurück"""
    return [step for step in flywheel_steps.values() if step["stage"] == stage_name]


def get_all_stages():
    """Gibt alle Stages mit ihren Schritten zurück"""
    return flywheel_info["stages"]


def print_flywheel_overview():
    """Gibt eine Übersicht über alle Schritte des Flywheel aus"""
    print("=" * 80)
    print(f"  {flywheel_info['name_de']}")
    print("=" * 80)
    print()
    
    for stage in flywheel_info["stages"]:
        print(f"\n{stage['name']} Stage:")
        print("-" * 80)
        for step_id in stage["steps"]:
            step = flywheel_steps[step_id]
            print(f"\n  Schritt {step['id']}: {step['title_de']} ({step['title']})")
            print(f"  {step['description_de'][:150]}...")
    
    print("\n" + "=" * 80)


